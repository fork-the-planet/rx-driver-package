/*****************************************************************************************
 * Function Name  @fn            cellular_free
 ****************************************************************************************/
void cellular_free(void * p_free)
{
    if (NULL != p_free)
    {
#if BSP_CFG_RTOS_USED == (1)
        vPortFree(p_free);
#elif BSP_CFG_RTOS_USED == (5)
        tx_block_release(p_free);
#endif
    }

    return;
}
