/*****************************************************************************************
 * Function Name  @fn            cellular_get_tickcount
 ****************************************************************************************/
uint32_t cellular_get_tickcount(void)
{
    uint32_t ret = 0;
#if BSP_CFG_RTOS_USED == (1)
    ret = xTaskGetTickCount();
#elif BSP_CFG_RTOS_USED == (5)
    ret = tx_time_get();
#endif

    return ret;
}
