/*************************************************************************************************
 * Function Name  @fn            atc_cmer
 ************************************************************************************************/
e_cellular_err_t atc_cmer(st_cellular_ctrl_t * const p_ctrl, const uint8_t mode)
{
    uint8_t          str[2]                                = {0};
    const uint8_t *  p_command_arg[CELLULAR_MAX_ARG_COUNT] = {0};
    e_cellular_err_t ret                                   = CELLULAR_SUCCESS;

    sprintf((char *)str, "%d", mode); // (uint8_t *)->(char *)

    p_command_arg[0] = str;

    atc_generate(p_ctrl->sci_ctrl.atc_buff, gp_at_command[ATC_SET_IND_NOTIFY], p_command_arg);

    ret = cellular_execute_at_command(p_ctrl, p_ctrl->sci_ctrl.atc_timeout, ATC_RETURN_OK, ATC_SET_IND_NOTIFY);

    return ret;
}
