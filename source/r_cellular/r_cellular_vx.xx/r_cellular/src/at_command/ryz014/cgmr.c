/*************************************************************************************************
 * Function Name  @fn            atc_cgmr
 ************************************************************************************************/
e_cellular_err_t atc_cgmr(st_cellular_ctrl_t * const p_ctrl)
{
    e_cellular_err_t ret = CELLULAR_SUCCESS;

    atc_generate(p_ctrl->sci_ctrl.atc_buff, gp_at_command[ATC_GET_SW_REVISION], NULL);

    ret = cellular_execute_at_command(p_ctrl, p_ctrl->sci_ctrl.atc_timeout, ATC_RETURN_OK, ATC_GET_SW_REVISION);

    return ret;
}
