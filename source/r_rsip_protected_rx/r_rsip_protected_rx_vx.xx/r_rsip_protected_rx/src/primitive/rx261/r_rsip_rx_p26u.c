rsip_ret_t r_rsip_p26u(const uint32_t InData_Program[], uint32_t MAX_CNT)
{
    int32_t iLoop = 0U, jLoop = 0U, kLoop = 0U, oLoop = 0U;
    uint32_t OFS_ADR = 0U;
    (void)iLoop;
    (void)jLoop;
    (void)kLoop;
    (void)oLoop;
    (void)OFS_ADR;
    RSIP.REG_0014H.WORD = 0x00020061U;
    RSIP.REG_00D4H.WORD = 0x40000000U;
    RSIP.REG_00D0H.WORD = 0x0e108456U;
    for (iLoop = 0; iLoop < MAX_CNT; iLoop = iLoop + 4)
    {
        /* WAIT_LOOP */
        while (1U != RSIP.REG_0014H.BIT.B31)
        {
            /* waiting */
        }
        RSIP.REG_002CH.WORD = InData_Program[iLoop + 0];
        RSIP.REG_002CH.WORD = InData_Program[iLoop + 1];
        RSIP.REG_002CH.WORD = InData_Program[iLoop + 2];
        RSIP.REG_002CH.WORD = InData_Program[iLoop + 3];
    }
    r_rsip_func205();
    r_rsip_func101(0x36c4e84fU, 0xf62516aaU, 0x6375e2f7U, 0x71b9934dU);
    return RSIP_RET_PASS;
}
