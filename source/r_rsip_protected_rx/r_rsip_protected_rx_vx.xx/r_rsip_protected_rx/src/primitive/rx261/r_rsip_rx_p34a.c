void r_rsip_p34a(const uint32_t InData_DataA[], uint32_t MAX_CNT)
{
    int32_t iLoop = 0U, jLoop = 0U, kLoop = 0U, oLoop = 0U;
    uint32_t OFS_ADR = 0U;
    (void)iLoop;
    (void)jLoop;
    (void)kLoop;
    (void)oLoop;
    (void)OFS_ADR;
    RSIP.REG_0014H.WORD = 0x00020061U;
    RSIP.REG_00D4H.WORD = 0x00008000U;
    RSIP.REG_00D0H.WORD = 0x0e128456U;
    for (iLoop = 0; iLoop < MAX_CNT; iLoop = iLoop + 4)
    {
        /* WAIT_LOOP */
        while (1U != RSIP.REG_0014H.BIT.B31)
        {
            /* waiting */
        }
        RSIP.REG_002CH.WORD = InData_DataA[iLoop + 0];
        RSIP.REG_002CH.WORD = InData_DataA[iLoop + 1];
        RSIP.REG_002CH.WORD = InData_DataA[iLoop + 2];
        RSIP.REG_002CH.WORD = InData_DataA[iLoop + 3];
    }
    r_rsip_func205();
    r_rsip_func101(0x71a4e8d7U, 0xc5c2e431U, 0x3383ec32U, 0xbc5bd731U);
}
