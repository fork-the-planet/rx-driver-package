    (void)oLoop;
    (void)OFS_ADR;
    if (0x0U != (RSIP.REG_006CH.WORD & 0x17U))
    {
        return RSIP_RET_RESOURCE_CONFLICT;
    }
    RSIP.REG_0070H.WORD = 0x00220001U;
    RSIP.REG_004CH.WORD = 0x00000000U;
    RSIP.REG_0078H.WORD = 0x00000203U;
