void r_rsip_func101(uint32_t ARG1, uint32_t ARG2, uint32_t ARG3, uint32_t ARG4)
{
    int32_t iLoop = 0U, jLoop = 0U, kLoop = 0U, oLoop = 0U;
    uint32_t OFS_ADR = 0U;
    (void)iLoop;
    (void)jLoop;
    (void)kLoop;
    (void)oLoop;
    (void)OFS_ADR;
    RSIP.REG_00D0H.WORD = 0x0a0701e5U;
    /* WAIT_LOOP */
    while (1U != RSIP.REG_0014H.BIT.B31)
    {
        /* waiting */
    }
    RSIP.REG_002CH.WORD = bswap_32big(ARG1);
    RSIP.REG_002CH.WORD = bswap_32big(ARG2);
    RSIP.REG_002CH.WORD = bswap_32big(ARG3);
    RSIP.REG_002CH.WORD = bswap_32big(ARG4);
    /* WAIT_LOOP */
    while (0U != RSIP.REG_00C8H.BIT.B17)
    {
        /* waiting */
    }
}
